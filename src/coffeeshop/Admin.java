  public void dmsimpan(){
      
        if (!dmnama.getText().equals(null) && !dmharga.equals(null) ){
            try{
                konek();
//                String reset = "ALTER TABLE id DROP id";
//                String consecutivenumbers = "ALTER TABLE transaksi ADD  id INT( 11 ) NOT NULL AUTO_INCREMENT FIRST ,ADD KEY (id)";
                sql="insert into menu (namamenu,harga,admin)"
                + "values(?,?,?)";
                pst=con.prepareStatement(sql);
//                con.createStatement().execute(reset);
//                con.createStatement().execute(consecutivenumbers);
                pst.setString(1, dmnama.getText().trim());
                pst.setString(2, dmharga.getText().trim());
                pst.setString(3, nama.getText().trim());
                pst.execute();
                dmnama.setText("");
                dmharga.setText("");
                JOptionPane.showMessageDialog(null, "Data Berhasil Di Input");
                dmtampil();
            }  catch (SQLException e){
                JOptionPane.showMessageDialog(null,"Gagal Mengisi data"+ e.getMessage());
            }
        }else {
            JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong");
        }
  }
