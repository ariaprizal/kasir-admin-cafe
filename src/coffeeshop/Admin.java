  public void dmtampil(){
            try{
                konek();
                String reset = "ALTER TABLE menu DROP id";
                String consecutivenumbers = "ALTER TABLE menu ADD id INT( 11 ) NOT NULL AUTO_INCREMENT FIRST ,ADD KEY (id)";
                con.createStatement().execute(reset);
                con.createStatement().execute(consecutivenumbers);
                TableColumn column;
                dmtable.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
                dmtable.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_NEXT_COLUMN);
                String header[]={"ID","Nama Menu ","Harga ","Admin"};
                model = new DefaultTableModel(header,0);
                dmtable.setModel(model);
                try{
                    stat =con.createStatement();
                    sql        = "SELECT * FROM  menu";
                    rs   = stat.executeQuery(sql);
                    while(rs.next ()){
                        String[] row = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)};
                        
                        model.addRow(row);
                    }
                }catch(SQLException err){
                    JOptionPane.showMessageDialog(null, err.getMessage() );
                }
            }catch(SQLException ex){
                Logger.getLogger(Admin.class.getName()).log(Level.SEVERE,null, ex);
            }
  }
